// ***Operators in JavaScripts***
// EX: In 3+4 '+' is the operator and 3, 4 are operand
// Operands: Entities on which operators operate

// 1. Unary Operator (It has single Operand : (x = -x))
c = -c;
console.log(c);
// 2. Binary Operator (It has single Operands : (x = x + 8))
c = 45 + 8;
console.log(c);

// ***Arithmetic Operators in JavaScripts***
var num1 = 2;
var num2 = 4;

console.log("The value num1 + num2 is : " + (num1 + num2));
console.log("The value num1 - num2 is : " + (num1 - num2));
console.log("The value num1 * num2 is : " + (num1 * num2));
console.log("The value num1 / num2 is : " + (num1 / num2));
console.log("The value (exponantiation operator) num1 ** num2 is : " + (num1 ** num2));

// ***Increment Operators in JavaScripts***
console.log("The value num1++ is : " + (num1++));
console.log("The value ++num1 is : " + (++num1));
console.log("The value num1-- is : " + (num1--));
console.log("The value --num1 is : " + (--num1));
