// JAVA SCRIPT DATA TYPES
// Data Types Forms:
// 1. Primitive data types:: numbers, strings, undefined, null, booleans, symbol
// 2. Reference data types:: objects, arrays

// Numbers
var num1 = 34;
var num2 = 56;
console.log(num1 + num2);

// Strings
var str1 = "This is a string";
var str2 = "This is also a string";
console.log(str1, str2);

// Objects
var marks = {
    ali: 10,
    kashif: 9,
    raees: 13.3
}
console.log(marks);

// Booleans
var a = true;
var b = false;
console.log(a, b);

// Undefined
var und1 = undefined;
var und2;
console.log(und1);
console.log(und2);

// NULL
var n = null;
console.log(n);

// Arrays
var arr = [1, 2, "Rayan", 4, 5];
console.log(arr);
console.log(arr[0]);
console.log(arr[1]);
console.log(arr[2]);
console.log(arr[3]);
console.log(arr[4]);