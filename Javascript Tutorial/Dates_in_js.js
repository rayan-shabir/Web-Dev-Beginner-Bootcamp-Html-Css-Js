// Generating current Date
let myDate = new Date();
console.log(myDate);

console.log(myDate.getTime()); //It will returns time in seconds
console.log(myDate.getFullYear()); //It will returns the hours of a specific date
console.log(myDate.getDay()); //It will returns the day of a specific date
console.log(myDate.getMinutes()); //It will returns the minutes of a specific date
console.log(myDate.getHours()); //It will returns the hours of a specific date