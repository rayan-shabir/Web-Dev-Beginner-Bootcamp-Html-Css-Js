// Operators in Java Scripts

// Arithmetic Operators
var a = 3;
var b = 5;

console.log("The value of a + b = ", a+b);
console.log("The value of a - b = ", a-b);
console.log("The value of a * b = ", a*b);
console.log("The value of a / b = ", a/b);

// Assignment Operators
var c = b;

console.log(c);
c += 2; // c = c + 2
c -= 2; // c = c - 2
c *= 2; // c = c * 2
c /= 2; // c = c / 2
console.log(c);

// Comparision Operators
var x = 36;
var y = 54;

console.log(x == y);
console.log(x >= y);
console.log(x <= y);
console.log(x < y);
console.log(x > y);

// Logical Operators
// AND Operator
console.log(true && true);
console.log(true && false);
console.log(false && false);
console.log(false && true);

// OR Operator
console.log(true || true);
console.log(true || false);
console.log(false || false);
console.log(false || true);

// NOT Operator
console.log(!true);
console.log(!false);